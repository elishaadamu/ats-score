import glob
import os
import re
from collections import Counter
from docx import Document

# ----------------------------
# READ DOCX FILE
# ----------------------------
def read_docx(path):
    doc = Document(path)
    return "\n".join([p.text for p in doc.paragraphs])

# ----------------------------
# KEYWORD EXTRACTION
# ----------------------------
stopwords = set([
    "the","and","or","to","of","in","a","an","for","with","on","at","by","is","are",
    "was","were","be","this","that","as","it","from","i","you","we","they","their"
])

def extract_keywords(text, top_n=12):
    words = re.findall(r"[a-zA-Z]+", text.lower())
    filtered = [w for w in words if w not in stopwords and len(w) > 2]
    counts = Counter(filtered)
    return [w for w, _ in counts.most_common(top_n)]

# ----------------------------
# FIND FILES
# ----------------------------
resume_file = "Jos_cv.docx"
job_files = sorted(glob.glob("J*.docx"))

if not job_files:
    raise Exception("No J*.docx files found in directory.")

resume_text = read_docx(resume_file)

# ----------------------------
# BUILD TAILORED RESUMES
# ----------------------------
for job_file in job_files:

    job_text = read_docx(job_file)
    keywords = extract_keywords(job_text)

    job_name = os.path.splitext(job_file)[0]

    doc = Document()

    # Title
    doc.add_heading(f"Tailored Resume - {job_name}", level=1)

    # Summary
    doc.add_heading("Professional Summary", level=2)
    doc.add_paragraph(resume_text[:900] + "...")

    # Skills Section (ATS Optimization)
    doc.add_heading("Key Skills Aligned to Job Description", level=2)
    for kw in keywords:
        doc.add_paragraph(f"• {kw}")

    # Optional enhancement section
    doc.add_heading("Professional Fit Summary", level=2)
    doc.add_paragraph(
        "This resume version is optimized to align with the job description "
        "based on extracted skills and keyword relevance."
    )

    # Save file
    output_file = f"tailored_resume_{job_name}.docx"
    doc.save(output_file)

    print(f"Created: {output_file}")


import docx2txt
import pandas as pd
import glob
import os
from sklearn.feature_extraction.text import CountVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# Load resume
resume = docx2txt.process("Jos_cv.docx")

# Dynamically get all job files like J1.docx, J2.docx, ...
job_files = sorted(glob.glob("J*.docx"))

results = []

for job_file in job_files:
    # Load job description
    job_description = docx2txt.process(job_file)

    # Combine texts
    text = [resume, job_description]

    # Vectorize
    cv = CountVectorizer()
    count_matrix = cv.fit_transform(text)

    # Similarity score
    similarity = cosine_similarity(count_matrix)[0][1] * 100
    similarity = round(similarity, 2)

    # Clean job name (remove .docx)
    job_name = os.path.splitext(job_file)[0]

    results.append({
        "Job": job_name,
        "Match %": similarity
    })

# Create table
df = pd.DataFrame(results)

# Sort by best match
df = df.sort_values(by="Match %", ascending=False).reset_index(drop=True)

print(df)

